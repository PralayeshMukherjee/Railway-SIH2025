export default function StickyHeadTable() {
  const [page, setPage] = React.useState(0);
  const [rowsPerPage, setRowsPerPage] = React.useState(10);

  const handleChangePage = (event: unknown, newPage: number) => {
    setPage(newPage);
  };

  const handleChangeRowsPerPage = (event: React.ChangeEvent<HTMLInputElement>) => {
    setRowsPerPage(+event.target.value);
    setPage(0);
  };

  return (
    <Paper sx={{ width: '100%', overflow: 'hidden', background: 'linear-gradient(to bottom right, rgba(17,24,39,0.7), rgba(39,39,42,0.6))',
    border: '1px solid rgba(34,211,238,0.2)',
    backdropFilter: 'blur(8px)',
   color: '#22d3ee', // Set text color for Paper
        '& .MuiTableCell-root': {
          color: '#22d3ee', // Set text color for all table cells
        },
        '& .MuiTableHead-root .MuiTableCell-root': {
          color: '#06b6d4', // Slightly brighter cyan for headers (optional)
          fontWeight: 700,}
 }}  >

      <TableContainer sx={{ maxHeight: 440  , color: "#22d3ee" }}>
        <Table stickyHeader aria-label="sticky table ">
          <TableHead>
            <TableRow >
              {columns.map((column) => (
                <TableCell
                     
                  key={column.id}
                  align={column.align}
                  style={{ minWidth: column.minWidth , background: 'linear-gradient(to bottom right, rgba(2,24,39,0.7), rgba(3,39,42,0.6))' }}
                >
                  {column.label}
                </TableCell>
              ))}
            </TableRow>
          </TableHead>
          <TableBody>
            {rows
              .slice(page * rowsPerPage, page * rowsPerPage + rowsPerPage)
              .map((row) => {
                return (
                  <TableRow hover role="checkbox" tabIndex={-1} key={row.code}>
                    {columns.map((column) => {
                      const value = row[column.id];
                      return (
                        <TableCell key={column.id} align={column.align}>
                          {column.format && typeof value === 'number'
                            ? column.format(value)
                            : value}
                        </TableCell>
                      );
                    })}
                  </TableRow>
                );
              })}
          </TableBody>
        </Table>
      </TableContainer>
      <TablePagination
        rowsPerPageOptions={[10, 25, 100]}
        component="div"
        count={rows.length}
        rowsPerPage={rowsPerPage}
        page={page}
        onPageChange={handleChangePage}
        onRowsPerPageChange={handleChangeRowsPerPage}
     
      />
    </Paper>
  );
}
